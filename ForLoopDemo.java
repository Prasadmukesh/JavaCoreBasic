 class ForLoopDemo {
    public static void main(String[] args) {
        // For Loop
        for(int c=1;c<=10;c++)
        {
            System.out.println(c+" Hare Rama");
        }
    System.out.println("While loop");
    //while loop
    int x=1;
    while(x<=10)
    {
        System.out.println(x+ " Hare Krishna");
        x++;
    }
    System.out.println("End of while loop");
    // do-while
    int j=1;
    do{
        System.out.println(j+ " Radhey Radhey");
        j++;

    }while(j<=10);


}
}

